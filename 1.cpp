//PROGRSM TO PRINT THE PATTERN

#include<iostream>
using namespace std;

int main()
{
    int i,j;// Initialized variables for the loop to print the pattern
    for(i=0;i<=4;i++)
 {


        for(j=0;j<=i;j++)
        {
         cout<<"*";
        }
        cout<<"\n";
}

    return 0;
}
